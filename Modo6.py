valor = float(input("Digite um valor: "))
reajuste = valor * 1.05
print(f"Valor com reajuste de 5%: {reajuste:.2f}")
# O código acima calcula o valor com um reajuste de 5% e imprime o resultado formatado com duas casas decimais.
# O código é simples e direto, utilizando a função input() para receber o valor do usuário e a formatação de strings para exibir o resultado.
# A formatação {:.2f} garante que o valor seja exibido com duas casas decimais, mesmo que o resultado seja um número inteiro ou tenha menos de duas casas decimais.